
public class Ex04 {

	public static void main(String[] args) {
		
		  // 1번 1~100 3의 배수 출력
        System.out.print("1~100 3의 배수  출력 : ");
        
        for(int i = 1; i <= 100; i++) {
            
            if (i%3 == 0)  {
                System.out.print(i + " ");
            }
        }
        

	}

}
